    public static string largestSegment(List<int> radius, int segments)
    {
        radius.Sort(Comparison);


        bool haveChanged = false;
        int index = 0;
        bool isOk = false;
        float testedRad = radius[0];
        float step = testedRad;
        while (index < 1000 || !isOk)
        {
            var curTestedRad = testedRad;
            var canApply = TryApply(curTestedRad, radius, segments);
            if (canApply)
            {
                curTestedRad += step;
            }
            else
            {
                curTestedRad -= step;
            }
            var canApply2 = TryApply(curTestedRad, radius, segments);
            var delta = curTestedRad - testedRad;
            if (canApply2 != canApply)
            {
                step = step * 0.5f;
                isOk = delta < 0.00001f;
                haveChanged = true;
            }
            else
            {
                if (!haveChanged)
                    step = step * 1.5f;
            }
            index++;
            Debug.LogError($"testedRad inner :{curTestedRad}  delta:{delta}  step:{step}");

            testedRad = curTestedRad;

        }



        Debug.LogError($"testedRad:{testedRad}");

        return testedRad.ToString("0.0000");
    }

    private static bool TryApply(float size, List<int> sorted, int segments)
    {
        foreach (var r in sorted)
        {
            var plCur = Math.PI * r * r;
//            Debug.LogError($"Circle:{plCur}");

            var counts = (int)(plCur / size);
//            Debug.LogError($"counts:{counts}");
            if (counts > 0)
            {
                segments -= counts;
                if (segments <= 0)
                {
                    return true;
                }
            }

        }

        return false;
    }

    private static int Comparison(int x, int y)
    {
        return x > y ? -1 : 1;
    }
